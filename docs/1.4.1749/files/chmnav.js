var s = "source/";

function getNav(op)
{
	var p = chmtop.c2wtopf.pageid;
	var n = s+p;
	var m = NV.length-1;

	for( i=0; i<=m; i++ )
	{
		if( NV[i] == p )
		{
			if ( op=="next" )
			{
				if ( i<m )
				{
					curpage=i+1;

					return s+NV[i+1];
				}
				else
				{
					return n;
				}
			}
			else
			{
				if( i > 0 )
				{
					curpage=i-1;

					return s+NV[i-1];
				}
				else
				{
					return n;
				}
			}
		}
	}

	return n;
}

